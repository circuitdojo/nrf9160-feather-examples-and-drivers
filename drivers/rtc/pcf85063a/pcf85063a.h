#define PCF85063A_RAM_BYTE 0x03
#define PCF85063A_TENS_SHIFT 4

/* Time related */
#define PCF85063A_SECONDS 0x04
#define PCF85063A_SECONDS_OS BIT(7)

#define PCF85063A_MINUTES 0x05

#define PCF85063A_HOURS 0x06
