#define PCF85063A_CTRL2 0x01
#define PCF85063A_CTRL2_AIE BIT(7)
#define PCF85063A_CTRL2_AF BIT(6)
#define PCF85063A_CTRL2_MI BIT(5)
#define PCF85063A_CTRL2_HMI BIT(4)
